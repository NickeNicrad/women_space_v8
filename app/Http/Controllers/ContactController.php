     *
     * @param  \App\Models\Contact  $contact
     * @return \Illuminate\Http\Response
     */
    public function show(Contact $contact)
    {
        $contacts = Contact::findOrFail($contact);

        return view('admin.contact_form', ['contact' => $contacts]);
    }

